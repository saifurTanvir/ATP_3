var db = require('./db');

module.exports= {
	validate: function(user, callback){
		var sql ="SELECT * FROM emplyee where username=? and password=?";
		db.getResults(sql, [user.username, user.password], function(results){

			if(results.length > 0){
				callback(true);
			}else{
				callback(false);
			}
		});
	}
}